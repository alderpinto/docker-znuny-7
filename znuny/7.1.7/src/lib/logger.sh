function customLogger() {
  TIMESTAMP=$(date +'%Y-%m-%d %H:%M:%S')
  LEVEL=${1}
  SOURCE=${2}
  MESSAGE=${3}

  cat << EOF
{"timestamp":"${TIMESTAMP}", "source":"${SOURCE}", "message":"${MESSAGE}"}
EOF
}
